            <h2 class="titleDescription">BIOGRAPHIE</h2>
            <div class="background">
                <div class="width_70 auto">
                    <p>Fils d’une photographe et d’un comédien et auteur, Tom Holland est l’aîné d’une famille de quatre garçons. <br><br>

                        Etudiant au Wimbledon College of Art, il débute ensuite une formation de deux ans à la BRIT School for Performing Arts & Technology.
                        Le jeune homme pratique également le hip-hop à la Nifty Feet Dance School de Wimbledon à Londres, et participe en 2006 au Richmond Dance
                        Festival, où il se fait repérer par la chorégraphe Lynne Page, célèbre notamment pour avoir été associée à la chorégraphie de la comédie
                        dramatique Billy Elliot (1999), avec Jamie Bell, et lauréate du César du meilleur film étranger en 2001. <br><br>

                        Le long-métrage de Stephen Daldry sera adapté en comédie musicale, orchestrée par Elton John et écrite par Lee Hall sous le nom de
                        "Billy Elliot The Musical". Après huit auditions et deux ans d’entraînement, le danseur en herbe décroche le rôle de Michael, le meilleur
                        ami de Billy, et livre sa première performance le 8 septembre 2008.<br><br>

                        Deux mois après la fin du musical, Tom Holland est approché par l’équipe du film-catastrophe espagnol The Impossible.
                        Il auditionne pour le rôle du fils aîné, Lucas, qu’il décroche dans la foulée. Le danseur britannique foule alors pour
                        la première fois un plateau de cinéma, aux côtés de l'actrice Naomi Watts, qu'il épaule dans un décor apocalyptique après
                        le Tsunami de 2004 en Thaïlande. Ce premier rôle lui vaut de nombreuses récompenses dont le Hollywood Spotlight Award,
                        la National Board of Review (Meilleure Révélation masculine) et le London Critics’ Circle (Meilleur jeune Acteur britannique de l'Année).<br><br>

                        En 2013, il est à l’affiche de How I Live Now (Maintenant c’est ma vie), où il joue Isaac, un binoclard de 14 ans qui va passer
                        des vacances d’été mouvementées. L'année suivante, Tom Holland donne la réplique à un autre Tom, l'acteur Tom Hardy dans le
                        thriller Locke, où un avocat voit sa vie basculer après un coup de téléphone. <br><br>

                        Le jeune Britannique est ensuite très sollicité. En décembre 2015, on le retrouvera à l'affiche du nouveau film de Ron Howard,
                        Au coeur de l'océan. Le comédien prendra la mer aux côtés de Chris Hemsworth, Cillian Murphy et Ben Whishaw dans le
                        rôle du jeune mousse Thomas Nickerson. L'histoire se déroulant au 19ème siècle raconte le périple maritime d'un baleinier
                        attaqué par un gigantesque cétacé. Cette catastrophe a inspiré à l'écrivain Herman Melville son plus célèbre récit, Moby Dick. <br><br>

                        Tom fera ensuite un nouveau bond dans le passé pour atterrir au 13ème siècle dans Pilgrimage ; il y incarnera un
                        moine devant escorter une relique sacrée dans une Irlande hostile. Richard Armitage et Jon Bernthal lui prêteront
                        main forte dans cette épopée.<br><br>

                        L'acteur reviendra ensuite à notre époque pour camper le fils de Joel Kinnaman dans Backcountry, drame retraçant
                        l'histoire d'un père tentant de renouer avec ses jeunes garçons. Entre-temps, Tom Holland fait une incursion sur
                        le petit écran pour la mini-série historique en 6 épisodes, Wolf Hall, aux côtés de Damian Lewis.<br><br>

                        Le 23 juin 2015, la carrière du jeune homme de 19 ans bascule quand Marvel annonce enfin une capitale décision :
                        c'est lui qui est retenu pour enfiler les collants de l'Homme-Araignée dans le reboot de la franchise Spider-Man !
                        Après un long processus de casting où l'acteur doit notamment passer des essais avec Chris Evans et Robert Downey Jr,
                        il décroche le Graal. Le personnage apparaît pour la première fois dans Captain America: Civil War, puis a droit à
                        son propre film en 2017, avec Spider-Man: Homecoming. Suivent Avengers: Infinity War, Endgame, Spider-Man: Far from Home et No Way Home. <br><br>

                        Parallèlement, Tom Holland joue le fils de l'aventurier Charlie Hunnam dans le brillant The Lost City of Z, un
                        jeune évoluant au sein d'une Amérique profonde faisant froid dans le dos dans Le Diable, tout le temps et un
                        ex-militaire devenu toxicomane dans Cherry. Trois prestations de haute volée. Il prête également son timbre à
                        des personnages présents dans Les Incognitos, Le Voyage du Dr Dolittle et En avant, puis campe les héros des
                        blockbusters d'action Chaos Walking et Uncharted.</p>
